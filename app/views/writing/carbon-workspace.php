    <h2>Carbon Workspace</h2>
    <h4 class="date">Posted: <date>27th October 2014</date></h4>

    <p>My workspace has been updated several times since it was featured on Lifehacker, after my summer trip to Iceland I put some A2 prints up on the walls, they look great, but the surface of my desk was looking worn, it was time for an update.</p>

    <ul>
        <li>Filling in the cut out, giving me more leg room.</li>
        <li>Adding a vinyl covering to the surface of the desk.</li>
        <li>Switching custom built NAS for a LaCie 10TB drive.</li>
        <li>Setting up my new Retina 5K iMac.</li>
    </ul>

    <div class="photo full-width">
        <img src="/assets/img/writing/carbon-workspace/desk.jpg">
    </div>

    <p>When fitting the vinyl covering, which is carbon fibre effect, I was worried this was going to look extremely <em>tacky</em>, however when it's being used I think it looks nice, it breaks up what was a sterile white area.</p>

    <h3>The Hardware</h3>
    <p><strong>AirPort Extreme</strong>, this can be seen on the far right of the desk, it's broadcasting on 2.4GHz and 5GHz.</p>
    <p><strong>Storage</strong>, I've moved from a custom built NAS, to a 10TB LaCie 5big drive which connects using Thunderbolt 2, this means I can browse photos straight from my storage device, something which was terribly slow with my NAS.</p>
    <p><strong>Nexus Wireless Charger</strong>, this is such a nice unit, place my phone on the pad, it charges, simple as that.</p>
    <p><strong>iMac</strong>, I've been pining for a retina screen for a while, the screen on this is beautiful! I've gone for the <em>4GHz i7</em> with <em>16GB RAM</em>.</p>
    <p><strong>MacBook Pro</strong>, this has been my primary machine since I bought it 21 months ago. <em>2.5GHz i5</em> with <em>8GB RAM</em>.</p>
    <p><strong>iPad 4</strong>, I don't really use the iPad that much day to day, but it's the perfect device for traveling.</p>


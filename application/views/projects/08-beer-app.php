		<h2>Beer App</h2>

		<ul class="details">
			<li><strong>Project Type:</strong> Side Project</li>
			<li><strong>Website:</strong> <a href="https://api.beerapp.net/" title="Beer App API" data-analytics="Beer App API">api.beerapp.net</a></li>
			<li><strong>Date:</strong> July 2014</li>
		</ul>

		<p>Myself and some friends wanted to build a mobile app called <em>Beer App</em> which would allow you to send your friends a request to meet up for a beer.</p>

		<p>It was decided we would each build our own app, taking different approaches. To save everyone time, and so we could focus on the app. I decided to build an API which each of us could use for our own implementation.</p>
		<p>The API is built using several composer packages (including a few of my own.) You can view the comprehensive API documentation at <a href="https://api.beerapp.net/" title="Beer App API" data-analytics="Beer App API">api.beerapp.net</a></p>

		<p>I decided to build an Android app with Cordova which interfaces with the API, and uses the phone contacts to easily search for friends. I utilised build hooks for SASS compilation, and signed the app, allowing me to upload it to the Google Play Store.</p>


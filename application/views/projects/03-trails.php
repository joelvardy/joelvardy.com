        <h2>Trails</h2>

        <ul class="details">
            <li><strong>Project Type:</strong> Side Project</li>
            <li><strong>Website:</strong> <a href="https://trail.sx/" title="Trails" data-analytics="Trails">trail.sx</a></li>
            <li><strong>Date:</strong> March 2014</li>
        </ul>

        <p>I'd had an idea for a few years that I could built something to map multiple bike rides and geolocated photos onto a single map.</p>

        <p>However it was after walking a few hills in Scotland that I was sat in a Caf&eacute;, and decided I could quickly knock something together, what I <a href="https://github.com/joelvardy/tracks.joelvardy.com" title="tracks.joelvardy.com repository" data-analytics="tracks.joelvardy.com repo">came up with</a> only plotted GPX files onto a Google Map.</p>

        <p>A few days later I decided to move it to it's own domain, and allow other people to use it, I also added the ability to plot photos onto the map.</p>


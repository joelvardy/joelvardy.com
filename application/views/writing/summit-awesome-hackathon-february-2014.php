	<h3>The Projects</h3>
	<p>There were several projects being worked on between the 25 people, the ones I remember:</p>
	<ul>
		<li><strong>CMYK</strong>: Real-time design feedback, <a href="http://cmyk.im" data-analytics="CMYK app">cmyk.im</a>.</li>
		<li><strong>XinPet</strong>: Managing pets microchips.</li>
		<li><strong>Chekk.it</strong>: Find the status of Minecraft plugins.</li>
	</ul>
	<p>No wanting to commit time to a new projects, I was working on finishing one of my own existing personal projects.</p>

	<h3>Timelapse</h3>
	<p>I really love timelapses, I set one up last year, and it was really well accepted, so I did one again this year.</p>
	<p>When I was trying to make the edit more interesting, I realised I should have taken some clips of people typing away ect.. But hey, I'm still learning and trying new things.</p>


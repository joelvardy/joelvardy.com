	<h3>The Burnout</h3>
	<p>I can't actually remember the last time I did an all nighter, so by 4am I was really feeling the lack of sleep. With almost everyone else asleep, I looked at what time the next train to Sheffield was, this set the seed of an idea, and I did decide to get the first train back home in the morning.</p>
	<p>This meant I unfortunately missed the presentations on Sunday afternoon, but I have to say my bed has never been so comfy!</p>

	<h3>Next Time</h3>
	<p>I really hope next time the days are forcibly spilt up, it would be additional cost for people who don't want to pay for somewhere to stay, but I think everyone would be more awake (and actually in attendance) on the Sunday.</p>
	<p>I really recommend attending a Summit Awesome meet up, either for food, drinks, or a hackathon. Find out more at <a href="http://summitawesome.co.uk/" title="Summit Awesome" data-analytics="Summit Awesome">summitawesome.co.uk</a>.</p>


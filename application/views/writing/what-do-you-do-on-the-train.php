	<h2>What Do You Do On The Train?</h2>
	<h4>Posted: <date>14th March 2014</date></h4>

	<p>Contracting outside of my home city, and not being a driver, means I'm frequently on trains, there are three things I do on trains.</p>
	<ul>
		<li>Stand up and get annoyed because I don't have a seat.</li>
		<li>Watch something on my iPad after a long day/week.</li>
		<li>However I usually try to learn/play around with something new.</li>
	</ul>
	<p>I'm writing this post while on the train to Manchester, this is the 7th time I've been on the train this week.</p>

	<h3>What Can I Work On?</h3>
	<p>I think the train is a perfect time to play with a bite-sized demo, sure you can work on your side project, but your mind is not fully focused on the task in hand while you have people making noises around you, conductors checking tickets ect..</p>
	<p>Some things I have played around with while on the train:</p>
	<ul>
		<li>My first play around with Compass.</li>
		<li>The first time I ditched jQuery was on the train.</li>
		<li>Get rough calculation of users download speed with JavaScript.</li>
		<li>Putting together a one-page CV style website (styled to look like Sublime Text.)</li>
		<li>Several node applications (uploading files to S3 / downloading torrents.)</li>
		<li>Giving <a href="https://daneden.me/toast/" title="Toast.css a Dan Eden project" data-analytics="toast.css">toast.css</a> a go.</li>
	</ul>
	<p>It doesn't have to be <em>something</em> you can launch as a website, just have a play around with something you haven't done before, or would like to do differently.</p>

	<h3>What About the Internet?</h3>
	<p>This is an interesting one, my train from Sheffield to Manchester is a data-dead zone for 80% of the journey. If you get stuck this can be annoying, but at the same time, it means you try more things before running to Google or Stack Overflow.</p>

	<h3>What Was The Point Of This Post?</h3>
	<p>On your next train journey, open up your laptop and try something new. If you're in an interview and the person opposite mentions a technology, the answer <quote>I've not used it commercially, but I played with it on the train once</quote> should make them realise you're someone who is keen to learn and interested.</p>
	<p>This doesn't just relate to web development, I'm sure you could try something new in Excel too.</p>

	<p>This week I've been playing around with <a href="http://demo.joelvardy.com/brightness-crop" title="JavaScript image manipulation demo" data-analytics="JavaScript image manipulation demo">simple image manipulation in JavaScript</a>.</p>


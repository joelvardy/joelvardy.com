	<h3>You Really Like Timelapse'</h3>
	<p>I think this is because they are pretty easy to accomplish, you set up a camera, then leave it alone. Post processing is a breeze compared to wading though loads of footage and putting it together in a meaningful way.</p>
	<p>I've found it's much easier to use my GoPro to take timelapse' rather then a dSLR. And it's certainly easier to mount the GoPro, for the above timelapse I mounted it on underside of my bed to achieve a birds-eye view of my development work.</p>
	<p>I also recorded the screen using Screenflick (at one frame a second) - however the app reported it would take 12 hours to export the video *sigh* - so I haven't added that video in the corner of this one as I had planned. Maybe next time.</p>

